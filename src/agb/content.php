    <div class="container mt-3 p-5 text-main">
        <h2>Richtlinien für Nutzer</h2>
        <ul>
            <li><strong>Korrekte Angaben:</strong> Nutzer sollten genaue und vollständige Informationen zu gemeldeten Problemen bereitstellen (z. B. Standort, Beschreibung).</li>
            <li><strong>Respektvoller Umgang:</strong> Alle Meldungen müssen sachlich und respektvoll formuliert sein. Beleidigende oder diskriminierende Inhalte sind nicht erlaubt.</li>
            <li><strong>Keine Missbrauchsmeldungen:</strong> Fehlmeldungen oder absichtliche Fehlangaben sind untersagt.</li>
            <li><strong>Angemessene Fotos:</strong> Hochgeladene Fotos sollten klar und relevant für das gemeldete Problem sein. Unangemessene Inhalte sind verboten.</li>
            <li><strong>Meldungen im öffentlichen Interesse:</strong> Nur Probleme in öffentlichen Bereichen (z. B. Müll, defekte Straßenbeleuchtung) dürfen gemeldet werden.</li>
        </ul>

        <h2>Haftung und Verantwortung</h2>
        <ul>
            <li><strong>Keine Haftung für Nichtbearbeitung:</strong> Die App garantiert nicht, dass gemeldete Probleme sofort oder überhaupt behoben werden.</li>
            <li><strong>Datensicherheit:</strong> Alle erfassten Daten werden vertraulich behandelt und nur zur Bearbeitung genutzt.</li>
        </ul>

        <h2>Datenspeicherung und Datenschutz</h2>
        <h3>Erfasste Daten</h3>
        <ul>
            <li><strong>Standortdaten:</strong> Der Standort des gemeldeten Problems (GPS-Koordinaten) wird gespeichert, um die genaue Lokalisierung zu ermöglichen.</li>
            <li><strong>Beschreibung der Meldung:</strong> Die eingegebene Beschreibung des Problems (z. B. "Kaputte Bank im Park").</li>
            <li><strong>Foto des Problems:</strong> Hochgeladene Bilder, die das Problem dokumentieren.</li>
            <li><strong>Zeitstempel:</strong> Datum und Uhrzeit der Meldung zur Priorisierung und Nachverfolgung.</li>
        </ul>

        <h3>Nicht gespeicherte Daten</h3>
        <ul>
            <li>Es werden <strong>keine personenbezogenen Daten</strong> wie Name, Adresse oder E-Mail-Adresse erfasst.</li>
            <li>Die Nutzung erfolgt anonym, da kein Benutzerkonto erforderlich ist.</li>
        </ul>
